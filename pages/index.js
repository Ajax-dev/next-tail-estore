import Head from 'next/head'
import Router  from 'next/router'
import Stripe from 'stripe'
import PurchaseCard from '../components/PurchaseCard'

export async function getServerSideProps(context) {
  const stripe = new Stripe(process.env.STRIPE_SECRET ?? '', {
    apiVersion: '2022-11-15'
  })

  const response = await stripe.prices.list({
    limit: 6,
    expand: ['data.product']
  })

  const prices = response.data.filter(price => price.active)

  return {
    //Renamed from prices to items as it is data of items
    props: {prices}
  }
}
export default function Home({ prices }) {
  console.log(prices)

  async function checkout() {
    const lineItems = [{
      price: prices[0].id,
      quantity: 1
    }]

    const res = await fetch('api/checkout', {
      method: 'POST',
      body: JSON.stringify({ lineItems })
    })

    const data = await res.json()
    // console.log(data)

   Router.push(data.session.url)
  }
  return (
    <div>
      <Head>
        <title>Marcy Trees Beta</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <p>RIP To Hello world</p>
      <div>Do you wish to buy...</div>
      <br></br>
      {prices.map((price, index) => {
        return <PurchaseCard price={price} key={index}/>
      })}

    </div>
  )
}
